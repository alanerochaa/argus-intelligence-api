-- TABELAS JAVA - INTELLIGENCE
-- ==========================================

CREATE TABLE BIOMA (
    ID_BIOMA NUMBER PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL UNIQUE,
    DESCRICAO VARCHAR2(500),
    AREA_KM2 NUMBER(15,2),
    NIVEL_RISCO_MEDIO VARCHAR2(30) DEFAULT 'BAIXO' NOT NULL,
    STATUS_MONITORAMENTO VARCHAR2(30) DEFAULT 'ATIVO' NOT NULL,
    DATA_CRIACAO DATE DEFAULT SYSDATE NOT NULL,
    DATA_ATUALIZACAO DATE,

    CONSTRAINT CK_BIOMA_AREA
        CHECK (AREA_KM2 IS NULL OR AREA_KM2 >= 0),

    CONSTRAINT CK_BIOMA_RISCO_MEDIO
        CHECK (NIVEL_RISCO_MEDIO IN ('BAIXO', 'MEDIO', 'ALTO', 'CRITICO')),

    CONSTRAINT CK_BIOMA_STATUS
        CHECK (STATUS_MONITORAMENTO IN ('ATIVO', 'INATIVO', 'EM_ANALISE'))
);

CREATE TABLE REGIAO (
    ID_REGIAO NUMBER PRIMARY KEY,
    NOME VARCHAR2(150) NOT NULL,
    ESTADO VARCHAR2(2) NOT NULL,
    CIDADE_REFERENCIA VARCHAR2(120),
    LATITUDE_CENTRAL NUMBER(10,6),
    LONGITUDE_CENTRAL NUMBER(10,6),
    NIVEL_RISCO VARCHAR2(20) DEFAULT 'BAIXO' NOT NULL,
    STATUS_MONITORAMENTO VARCHAR2(30) DEFAULT 'ATIVA' NOT NULL,
    DATA_CRIACAO DATE DEFAULT SYSDATE NOT NULL,
    DATA_ATUALIZACAO DATE,
    ID_BIOMA NUMBER NOT NULL,

    CONSTRAINT CK_REGIAO_LATITUDE
        CHECK (LATITUDE_CENTRAL IS NULL OR LATITUDE_CENTRAL BETWEEN -90 AND 90),

    CONSTRAINT CK_REGIAO_LONGITUDE
        CHECK (LONGITUDE_CENTRAL IS NULL OR LONGITUDE_CENTRAL BETWEEN -180 AND 180),

    CONSTRAINT CK_REGIAO_RISCO
        CHECK (NIVEL_RISCO IN ('BAIXO', 'MEDIO', 'ALTO', 'CRITICO')),

    CONSTRAINT CK_REGIAO_STATUS
        CHECK (STATUS_MONITORAMENTO IN ('ATIVA', 'INATIVA', 'EM_ANALISE')),

    CONSTRAINT FK_REGIAO_BIOMA
        FOREIGN KEY (ID_BIOMA)
        REFERENCES BIOMA(ID_BIOMA)
);

CREATE TABLE FOCO_CALOR (
    ID_FOCO NUMBER PRIMARY KEY,
    LATITUDE NUMBER(10,6) NOT NULL,
    LONGITUDE NUMBER(10,6) NOT NULL,
    FRP NUMBER(10,2),
    TEMPERATURA_ESTIMADA NUMBER(6,2),
    CONFIANCA VARCHAR2(30),
    SATELITE VARCHAR2(80),
    SENSOR VARCHAR2(80),
    ORIGEM_DADO VARCHAR2(80) DEFAULT 'NASA FIRMS' NOT NULL,
    DATA_HORA DATE NOT NULL,
    STATUS VARCHAR2(30) DEFAULT 'ATIVO' NOT NULL,
    PAYLOAD_JSON CLOB,
    DATA_CRIACAO DATE DEFAULT SYSDATE NOT NULL,
    DATA_ATUALIZACAO DATE,
    ID_REGIAO NUMBER NOT NULL,

    CONSTRAINT CK_FOCO_LATITUDE
        CHECK (LATITUDE BETWEEN -90 AND 90),

    CONSTRAINT CK_FOCO_LONGITUDE
        CHECK (LONGITUDE BETWEEN -180 AND 180),

    CONSTRAINT CK_FOCO_FRP
        CHECK (FRP IS NULL OR FRP >= 0),

    CONSTRAINT CK_FOCO_STATUS
        CHECK (STATUS IN ('ATIVO', 'MONITORADO', 'ENCERRADO', 'DESCARTADO')),

    CONSTRAINT CK_FOCO_CONFIANCA
        CHECK (CONFIANCA IS NULL OR CONFIANCA IN ('BAIXA', 'MEDIA', 'ALTA', 'CRITICA')),

    CONSTRAINT FK_FOCO_REGIAO
        FOREIGN KEY (ID_REGIAO)
        REFERENCES REGIAO(ID_REGIAO)
);

CREATE TABLE ALERTA (
    ID_ALERTA NUMBER PRIMARY KEY,
    TITULO VARCHAR2(150) NOT NULL,
    DESCRICAO VARCHAR2(500),
    NIVEL VARCHAR2(20) NOT NULL,
    STATUS VARCHAR2(30) DEFAULT 'ABERTO' NOT NULL,
    SCORE_RISCO NUMBER(5,2),
    RECOMENDACAO_OPERACIONAL VARCHAR2(1000),
    DATA_GERACAO DATE DEFAULT SYSDATE NOT NULL,
    DATA_ATUALIZACAO DATE,
    ID_FOCO NUMBER NOT NULL,

    CONSTRAINT CK_ALERTA_SCORE
        CHECK (SCORE_RISCO IS NULL OR SCORE_RISCO BETWEEN 0 AND 100),

    CONSTRAINT CK_ALERTA_NIVEL
        CHECK (NIVEL IN ('BAIXO', 'MEDIO', 'ALTO', 'CRITICO')),

    CONSTRAINT CK_ALERTA_STATUS
        CHECK (STATUS IN ('ABERTO', 'EM_ANALISE', 'ENCAMINHADO', 'ENCERRADO')),

    CONSTRAINT FK_ALERTA_FOCO
        FOREIGN KEY (ID_FOCO)
        REFERENCES FOCO_CALOR(ID_FOCO)
);

